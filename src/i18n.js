export function pickLang(obj, field, language) {
  if (!obj) return "";
  return obj[`${field}_${language}`] || obj[`${field}_pt`] || obj[field] || "";
}

export function localizeItem(item, language) {
  if (!item) return item;
  return {
    ...item,
    title: pickLang(item, "title", language),
    summary: pickLang(item, "summary", language),
    detail: pickLang(item, "detail", language),
    status: pickLang(item, "status", language),
    level: pickLang(item, "level", language),
    change: pickLang(item, "change", language),
    type: pickLang(item, "type", language),
    date: item[`date_${language}`] || item.date || "",
    recurrenceDescription:
      item[`recurrenceDescription_${language}`] ||
      item.recurrenceDescription_pt ||
      item.recurrenceDescription ||
      "",
  };
}

export function localizeEdition(rawEdition, language) {
  if (!rawEdition) return rawEdition;

  const mapItems = (items = []) => items.map((item) => localizeItem(item, language));

  return {
    ...rawEdition,
    title: pickLang(rawEdition, "title", language),
    summary: pickLang(rawEdition, "summary", language),
    month: rawEdition[`month_${language}`] || rawEdition.month || "",
    period: rawEdition[`period_${language}`] || rawEdition.period || rawEdition.month || "",
    highlights: mapItems(rawEdition.highlights),
    aneelAgenda: mapItems(rawEdition.aneelAgenda),
    publishedRules: mapItems(rawEdition.publishedRules),
    aneelTopics: mapItems(rawEdition.aneelTopics),
    mmeTopics: mapItems(rawEdition.mmeTopics),
    publicParticipation: mapItems(rawEdition.publicParticipation),
    auctions: mapItems(rawEdition.auctions),
    agenda: rawEdition.agenda
      ? {
          ...rawEdition.agenda,
          month: rawEdition.agenda[`month_${language}`] || rawEdition.agenda.month || "",
          events: mapItems(rawEdition.agenda.events || []),
        }
      : rawEdition.agenda,
  };
}

export function localizeEditionMeta(meta, language) {
  if (!meta) return meta;
  return {
    ...meta,
    label: meta[`label_${language}`] || meta.label_pt || meta.label || "",
  };
}
